// ─── 중복 제거 (공백 기준 정규화, total 높은 것 우선) ───────────────────────
function deduplicateKeywords(data: KeywordData[]): KeywordData[] {
  const seen = new Map<string, KeywordData>();
  for (const k of data) {
    const normalized = k.keyword.toLowerCase().replace(/\s+/g, "");
    const existing = seen.get(normalized);
    if (!existing || k.total > existing.total) {
      seen.set(normalized, k);
    }
  }
  return Array.from(seen.values());
}

export function searchKeywords(query: string): KeywordData[] {
  if (!query.trim()) return deduplicateKeywords(KEYWORD_DB).slice(0, 50);
  const q = query.toLowerCase().replace(/\s+/g, "");
  const matched = KEYWORD_DB.filter(k =>
    k.keyword.toLowerCase().replace(/\s+/g, "").includes(q)
  );
  return deduplicateKeywords(matched).sort((a, b) => b.total - a.total);
}

export function getTopKeywords(limit: number): KeywordData[] {
  return [...KEYWORD_DB].sort((a, b) => b.mobile - a.mobile).slice(0, limit);
}

// [P0-2/P1-1] 음수·경계값 안전 처리 (9999→9천, -5000→-5천)
export function formatVol(n: number): string {
  if (!Number.isFinite(n)) return "—";
  const abs = Math.abs(n);
  const sign = n < 0 ? "-" : "";
  if (abs >= 10000) return `${sign}${(abs / 10000).toFixed(1)}만`;
  if (abs >= 1000) return `${sign}${Math.floor(abs / 1000)}천`;
  return String(n);
}
