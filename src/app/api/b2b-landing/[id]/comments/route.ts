function randomDate(daysBack: number): Date {
  const now = new Date();
  const offset = Math.floor(Math.random() * daysBack * 24 * 60 * 60 * 1000);
  return new Date(now.getTime() - offset);
}

function pickRandom<T>(arr: T[], count: number): T[] {
  const shuffled = [...arr].sort(() => Math.random() - 0.5);
  return shuffled.slice(0, count);
}

// ── GET ─────────────────────────────────────────────────

export async function GET(
  _req: NextRequest,
  context: { params: Promise<{ id: string }> },
) {
  try {
    const session = await getMabizSession();
    if (!session) {
      return NextResponse.json({ ok: false, message: '인증이 필요합니다.' }, { status: 401 });
    }

    const { id } = await context.params;

    const comments = await prisma.b2BLandingComment.findMany({
      where: { landingPageId: id },
      orderBy: { createdAt: 'desc' },
    });

    return NextResponse.json({ ok: true, comments });
  } catch (err) {
    return NextResponse.json({ ok: false, message: '댓글 조회 실패' }, { status: 500 });
  }
}

// ── POST ────────────────────────────────────────────────

export async function POST(
  req: NextRequest,
  context: { params: Promise<{ id: string }> },
) {
  try {
    const session = await getMabizSession();
    if (!session) {
      return NextResponse.json({ ok: false, message: '인증이 필요합니다.' }, { status: 401 });
    }

    const { id } = await context.params;
    const body = await req.json();

    // 페이지 존재 확인
    const page = await prisma.b2BLandingPage.findFirst({
      where: { id, ...(session.organizationId ? { organizationId: session.organizationId } : {}) },
      select: { id: true },
    });
    if (!page) {
      return NextResponse.json({ ok: false, message: 'B2B 랜딩페이지를 찾을 수 없습니다.' }, { status: 404 });
    }

    // AI 자동 생성 모드
    if (body.generate === true) {
      const count = [3, 5, 7, 10].includes(body.count) ? body.count : 3;

      const names = pickRandom(KOREAN_NAMES, count);
      const reviews = pickRandom(REVIEW_TEMPLATES, count);

      const comments = await prisma.$transaction(
        names.map((name, i) =>
          prisma.b2BLandingComment.create({
            data: {
              landingPageId: id,
              authorName: name,
              content: reviews[i],
              isAutoGenerated: true,
              createdAt: randomDate(30),
            },
          }),
        ),
      );

      return NextResponse.json({ ok: true, comments });
    }

    // 단건 작성
    const { authorName, content } = body;
    if (!authorName || !content) {
      return NextResponse.json({ ok: false, message: 'authorName, content는 필수입니다.' }, { status: 400 });
    }

    const comment = await prisma.b2BLandingComment.create({
      data: {
        landingPageId: id,
        authorName,
        content,
        isAutoGenerated: false,
      },
    });

    return NextResponse.json({ ok: true, comment });
  } catch (err) {
    return NextResponse.json({ ok: false, message: '댓글 생성 실패' }, { status: 500 });
  }
}

// ── DELETE ───────────────────────────────────────────────

export async function DELETE(
  req: NextRequest,
  context: { params: Promise<{ id: string }> },
) {
  try {
    const session = await getMabizSession();
    if (!session) {
      return NextResponse.json({ ok: false, message: '인증이 필요합니다.' }, { status: 401 });
    }

    // 권한 확인: GLOBAL_ADMIN 또는 OWNER만 삭제 가능
    if (session.role !== 'GLOBAL_ADMIN' && session.role !== 'OWNER') {
      return NextResponse.json({ ok: false, message: '삭제 권한이 없습니다.' }, { status: 403 });
    }

    const { id } = await context.params;
    const { searchParams } = new URL(req.url);
    const commentId = searchParams.get('commentId');

    if (!commentId) {
      return NextResponse.json({ ok: false, message: 'commentId 쿼리 파라미터가 필요합니다.' }, { status: 400 });
    }

    // 해당 랜딩페이지 소속 댓글인지 확인
    const comment = await prisma.b2BLandingComment.findFirst({
      where: { id: commentId, landingPageId: id },
    });
    if (!comment) {
      return NextResponse.json({ ok: false, message: '댓글을 찾을 수 없습니다.' }, { status: 404 });
    }

    await prisma.b2BLandingComment.delete({ where: { id: commentId } });

    return NextResponse.json({ ok: true });
  } catch (err) {
    return NextResponse.json({ ok: false, message: '댓글 삭제 실패' }, { status: 500 });
  }
}
