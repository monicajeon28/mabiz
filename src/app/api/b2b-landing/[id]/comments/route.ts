export const dynamic = 'force-dynamic';

import { NextResponse } from 'next/server';
import prisma from '@/lib/prisma';
import { getAuthContext, requireOrgId } from '@/lib/rbac';
import { logger } from '@/lib/logger';
import { getCache, setCache } from '@/lib/redis';

type Params = { params: Promise<{ id: string }> };

/**
 * GET /api/b2b-landing/[id]/comments?skip=0&limit=10
 * B2B лһңл”©нҺҳмқҙм§Җ AI мғқм„ұ нӣ„кё° лӘ©лЎқ мЎ°нҡҢ
 *
 * Query params:
 * - skip: нҺҳмқҙм§Җ мҳӨн”„м…Ӣ (кё°ліё 0)
 * - limit: нҺҳмқҙм§Җ нҒ¬кё° (кё°ліё 10, мөңлҢҖ 50)
 *
 * Response: { ok: true, comments: [...], total: number, totalPages: number }
 */
export async function GET(req: Request, { params }: Params) {
  try {
    const ctx = await getAuthContext();
    const orgId = requireOrgId(ctx);
    const { id } = await params;

    // B2B лһңл”©нҺҳмқҙм§Җ мҶҢмң к¶Ң нҷ•мқё
    const page = await prisma.b2BLandingPage.findFirst({
      where: { id, organizationId: orgId },
      select: { id: true },
    });
    if (!page) {
      return NextResponse.json({ ok: false, error: 'NOT_FOUND', message: 'лһңл”©нҺҳмқҙм§ҖлҘј м°ҫмқ„ мҲҳ м—ҶмҠөлӢҲлӢӨ.' }, { status: 404 });
    }

    // мҝјлҰ¬ нҢҢлқјлҜён„° нҢҢмӢұ
    const { searchParams } = new URL(req.url);
    const skip = Math.min(10000, Math.max(0, parseInt(searchParams.get('skip') ?? '0') || 0));
    const rawLimit = parseInt(searchParams.get('limit') ?? '10') || 10;
    const limit = Math.min(50, Math.max(1, rawLimit));

    // [мәҗмӢұ] Redisм—җм„ң 5л¶„ TTLлЎң мәҗмӢңлҗң лҢ“кёҖ лӘ©лЎқ мЎ°нҡҢ
    const cacheKey = `b2b:comments:${id}:${skip}:${limit}`;
    const cachedComments = await getCache<any>(cacheKey);
    if (cachedComments) {
      logger.log('[B2B comments] мәҗмӢңм—җм„ң мЎ°нҡҢ', { id, orgId, skip, limit });
      return NextResponse.json(cachedComments);
    }

    // м „мІҙ лҢ“кёҖ мҲҳ
    const total = await prisma.b2BLandingComment.count({
      where: { landingPageId: id },
    });

    // лҢ“кёҖ мЎ°нҡҢ
    const comments = await prisma.b2BLandingComment.findMany({
      where: { landingPageId: id },
      orderBy: { createdAt: 'desc' },
      skip,
      take: limit,
      select: {
        id: true,
        authorName: true,
        content: true,
        isAutoGenerated: true,
        createdAt: true,
      },
    });

    const totalPages = Math.ceil(total / limit);

    logger.log('[GET /api/b2b-landing/[id]/comments]', {
      landingPageId: id,
      orgId,
      skip,
      limit,
      total,
      returned: comments.length,
    });

    const response = {
      ok: true,
      data: comments,
      total,
      totalPages,
    };

    // [мәҗмӢұ] мқ‘лӢөмқ„ 5л¶„ TTLлЎң Redisм—җ мәҗмӢң
    await setCache(cacheKey, response, 300);

    return NextResponse.json(response);
  } catch (err) {
    const errorMsg = err instanceof Error ? err.message : String(err);
    const errorCode = err instanceof Error && 'code' in err ? (err as any).code : 'UNKNOWN';
    logger.error('[GET /api/b2b-landing/[id]/comments] Error', {
      error: errorMsg,
      errorCode,
      stack: err instanceof Error ? err.stack?.split('\n').slice(0, 3).join('\n') : undefined,
    });
    return NextResponse.json({ ok: false, error: 'INTERNAL_ERROR', message: 'лҢ“кёҖмқ„ л¶Ҳлҹ¬мҳӨм§Җ лӘ»н–ҲмҠөлӢҲлӢӨ.' }, { status: 500 });
  }
}
